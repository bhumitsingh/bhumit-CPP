#include <iostream>
using namespace std;

int main(){
    int a = 5; // integer
    float b = 5.5; // floating point
    double c = 5.000; // double floating point
    char d = 'a'; // character
    bool e = true; // boolean

    cout << "The value of a(an integer) is: " << a << endl;
    cout << "The value of b(a floating point) is: " << b << endl;
    cout << "The value of c(a double floating point) is: " << c << endl;
    cout << "The value of d(a character) is: " << d << endl;
    cout << "The value of e(a boolean) is: " << e << endl;

    return 0;
}